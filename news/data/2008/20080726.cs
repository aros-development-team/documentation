Mise splněna!
-------------

I když je poslední "Stav aktualizace" teprve týden starý,
objevily se dvě velké novinky a máme tu nový.

Michal Schulz dokončil port nativního AROSu na základní desku
"Samantha" SAM440EP. To je poprvé, kdy je zcela podporována jiná než
x86 základní deska, a dělá to z AROSu operační systém, který běží
na více platformách, tak jako Linux. ACube Systems, výrobce a
prodejce desky SAM440EP přikládá AROS Live CD k
jejich vlajkovým produktům. Dřívější kupci Samantha platformy si mohou
stáhnout svoji verzi AROSu z `Michalovy stránky`__. Lidé
zajímající se o koupi této alternativní platformy ji teď mohou získat
za `cenu s letní slevou`__.

Krzysztof Smiechowicz dokončil své úsilí o přezkoumání aktuální
úplnosti knihoven a kompatibility s původní AmigaOS 3.1
API, s celkovým skóre 89%. To znamená, že jeden z cílů AROSu
je téměř dosažen. Aktuální stav můžeš vidět `zde`__.

