================
Stav aktualizace
================

:Autor:   Paolo Besser
:Datum:   17.06.2007

Poslední zprávy
---------------

Rozšiřování AROSu po světě pokračuje! Ioannis Koniaris přeložil `AROS.org do Řečtiny`__.

V posledních týdnech jsme měli technické potíže s naším sestavovacím strojem,
omlouváme se, pokud jsi nemohl stáhnout aktualizované noční sestavení. Problém
byl vyřešen se zbrusu novým sestavovacím strojem, o což se postaral Robert Norris.

Na "Team AROS" byly nedávno otevřeny tři nové zajímavé "bounties":
podívej se prosím na `AROS na SAM440`__, `validátor disku`__ a
`vytvoření nové AROS distribuce`__.

Chceme nesmírně poděkovat všem, kdo jsou zapojeni do chodu AROSu:
v posledních týdnech jsme do naší `soutěže`__ obdželi spoustu wallpaperů a
také nějaké nové `bannery`__. Pokud máš vlastní web, nebo jsi aktivním účastníkem
diskuzních fór, můžeš pomoci AROSu proniknout použitím odkazů na (tvoji jazykovou
verzi) Aros.org.


