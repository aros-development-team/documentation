=====================
AROS na AmiWestu 2012
=====================

:Autor:   Jason McMullan
:Datum:   2012-11-02

V rámci akce AmiWest 2012, Samuel Crow a Jason McMullan předvedli AROS v1
běžící na systémech Sam460ex od `ACube`__, FPGA Arcade Replay od
`FPGA Arcade`__ a na Raspberry PI od `Raspberry PI Foundation`__.

Jason McMullan měl 20-minutovou  `presentation`__ ohledně původu a současného
vývoje AROSu, a byl členem diskuzní skupiny debatující na téma současného
stavu a budoucnosti operačních systémů z rodiny AmigaOS.

Během show byla všem příchozím rozdávána DVD s distribucemi `Icaros`__
(pc-i386, ABI v0) a `AROS Vision`__ (amiga-m68k).


