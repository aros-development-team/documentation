======================
Aktualności świąteczne
======================

:Autor:   Paolo Besser
:Data:     2006-12-23

Wesołych Świąt Bożego Narodzenia i Szczęśliwego Nowego Roku! To był 
ważny rok w rozwoju AROSa i chcemy podziękować wszystkim fanom 
AROSa, testerom i współpracownikom za wyśmienite wyniki, jakie  
osiągnął nasz system operacyjny.

W oczekiwaniu na pierwsze wieści z 2007 roku dlaczego nie posłuchać 
kolęd z AROSAmp, pierwszego odtwarzacza mp3 z graficznym interfejsem 
użytkownika dla AROSa? Możesz go znaleźć `tutaj`__

A jeśli chcesz obejrzeć swoje stare zdjęcia cyfrowe, może zainteresujesz 
się `przeglądarką Pawła Stefanskiego`__ dla Morphosa, który został 
ostatnio portowany na AROSa. 

