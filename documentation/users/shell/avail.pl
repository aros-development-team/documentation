Avail
=====

Składnia
~~~~~~~~
::


	CHIP/S, FAST/S, TOTAL/S, FLUSH/S, H=HUMAN/S        


Ścieżka
~~~~~~~
::


	Workbench:C


Funkcja
~~~~~~~
::

	Podaje podsumowanie pamięci, użytej i dostępnej w systemie. Aby
	wyczyścić nieużywaną pamięć, która wciąż może być wykorzystywana
	(przez biblioteki, urządzenia, czcionki i inne, które występują
	w pamięci, ale nie są używane), użyj opcji FLUSH.


Parametry
~~~~~~~~~
::


	CHIP	--	pokaż tylko pamięć CHIP (graficzną)
	FAST	--	pokaż tylko pamięć FAST (pamięć operacyjna)
	TOTAL	--	pokaż pamięć bez względu na rodzaj
	FLUSH	--	wymaż nieaktywne części pamięci
	HUMAN	--	wyświetl w wartościach zrozumiałych dla użytkownika
			(K jak kilobajty, M jak megabajty i G jak gigabajty)


Opis
~~~~
::

	Pamięci CHIP i FAST są dostępne dla komputerów Amiga i mogą być
	niedostępne dla Twojej platformy sprzętowej.

 
